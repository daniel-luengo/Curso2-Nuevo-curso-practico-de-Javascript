let nombre;
let apellido;

function nombreCompleto (nombre , apellido) {
    return nombre + " " + apellido;
}

nombreCompleto("Daniel", "Broom");




function nombreNickname(nombre, apellido, nickname) {
    return ("Minombre es " + nombre + " " + apellido + " pero prefiero que me llames " + nickname);
}

nombreNickname("Daniel " + " Luengo" + " Kaneki")